    private void btnSiguienteMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnSiguienteMouseClicked
        Acceso acc = new Acceso();
        String usuario = txtUser.getText();
        String contra = txtContra.getText();
        String resultado[] =acc.validar(usuario, contra);
        Vista_Principal principal=new Vista_Principal();
        principal.nombre=resultado[0];
        principal.setExtendedState(MAXIMIZED_BOTH);
        if (resultado[1].equals("1")) {
            //JOptionPane.showMessageDialog(this, "Usted es un Administrador");
            principal.sesion=resultado[1];
            //principal.setLocationRelativeTo(null);
            principal.setVisible(true);
            this.dispose();
        }else if (resultado[1].equals("2")) {
            //JOptionPane.showMessageDialog(this, "Usted es un Usuario");
            principal.sesion=resultado[1];
            //principal.setLocationRelativeTo(null);
            principal.setVisible(true);
            this.dispose();
        } else {
            JOptionPane.showMessageDialog(this, "Datos Incorrectos");
        }
    }//GEN-LAST:event_btnSiguienteMouseClicked

